        <h2 class="text-center">Сайт-визитка</h2>
        <p class="text-justify"><strong>Сайт-визитка</strong> (англ. business card website) — небольшой сайт, как
            правило, состоящий из пяти, семи веб-страниц и содержащий основную информацию об организации, частном лице,
            компании, товарах или услугах, прейскуранты, контактные данные.

            Сайт-визитка отлично подходит малому бизнесу или стартапам. Компаниям, которые только начинают свой путь,
            не имея больших бюджетов. При этом нельзя сравнивать сайт визитку с страницей-заглушкой. Хотя бы потому, что
            сайт-визитку при правильно продуманной структуре сайта и верной SEO оптимизации, можно продвинуть в
            поисковой выдаче поисковых систем (по узкому списку ключей), тогда поисковые машины начнут приводить на сайт
            трафик, который будет конвертироваться в потенциальных клиентов и приносить прибыль. Так же можно на него
            приводить трафик с помощью контекстной рекламы. В случае с страницей-заглушкой, вероятность успеха в её
            SEO-продвижении равна нулю, да и вероятность, что трафик приведенный с контекстной рекламы конвертируется в
            потенциальных клиентов - крайне минимальная.
        </p>
