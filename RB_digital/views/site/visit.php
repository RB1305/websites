        <h4 class="text-left">Реализации</h4>
        <p class="text-justify">Сайт-визитка может быть как динамическим, так и статическим сайтом.

            Часто содержат значительные объёмы графики, в том числе анимации или Flash.

            Ещё одной разновидностью сайтов-визиток являются сайты, созданные с применением т. н. Flash-технологий,
            когда весь сайт-визитка располагается на одной веб-странице, предназначенной исключительно для загрузки
            Flash-приложения, а вся навигация и содержимое выполнены в самом Flash-ролике.
